import 'package:doctorbike/core/helpers/json_safe_parser.dart';
import 'package:doctorbike/features/admin/employee_section/domain/entities/financial_details_entity.dart';

import '../../../../../core/databases/api/end_points.dart';

class FinancialDetailsModel extends FinancialDetailsEntity {
  const FinancialDetailsModel({
    required int employeeId,
    required String employeeName,
    required String salary,
    required String debts,
    required String points,
    required String hourWorkPrice,
    required String totalWorkHours,
    required String numberOfWorkHours,
    required dynamic pointsRevenue,
    required dynamic total,
    required String month,
    required String selectedMonth,
    required String baseSalary,
    required String attendanceDays,
    required String absentDays,
    required String lateDays,
    required String delayHours,
    required String overtimeHours,
    required String overtimeSalary,
    required String deductions,
    required String bonuses,
    required String additions,
    required String advances,
    required String finalNetEntitlement,
  }) : super(
          employeeId: employeeId,
          employeeName: employeeName,
          salary: salary,
          debts: debts,
          points: points,
          hourWorkPrice: hourWorkPrice,
          totalWorkHours: totalWorkHours,
          numberOfWorkHours: numberOfWorkHours,
          pointsRevenue: pointsRevenue,
          total: total,
          month: month,
          selectedMonth: selectedMonth,
          baseSalary: baseSalary,
          attendanceDays: attendanceDays,
          absentDays: absentDays,
          lateDays: lateDays,
          delayHours: delayHours,
          overtimeHours: overtimeHours,
          overtimeSalary: overtimeSalary,
          deductions: deductions,
          bonuses: bonuses,
          additions: additions,
          advances: advances,
          finalNetEntitlement: finalNetEntitlement,
        );

  factory FinancialDetailsModel.fromJson(Map<String, dynamic> json) {
    final j = Map<String, dynamic>.from(json);
    return FinancialDetailsModel(
      employeeId: asInt(j[ApiKey.employee_id]),
      employeeName: asString(j[ApiKey.employee_name], 'unknown'),
      salary: asString(j[ApiKey.salary], '0'),
      debts: asString(j[ApiKey.debts], '0'),
      points: asString(j[ApiKey.points], '0'),
      hourWorkPrice: asString(j[ApiKey.hour_work_price], '0'),
      totalWorkHours: asString(j[ApiKey.total_work_hours], '0'),
      numberOfWorkHours: asString(j[ApiKey.number_of_work_hours], '0'),
      pointsRevenue: j[ApiKey.points_revenue],
      total: j[ApiKey.total],
      month: asString(j['month'], ''),
      selectedMonth: asString(j['selected_month'], ''),
      baseSalary: asString(j['base_salary'] ?? j[ApiKey.salary], '0'),
      attendanceDays: asString(j['attendance_days'], '0'),
      absentDays: asString(j['absent_days'], '0'),
      lateDays: asString(j['late_days'], '0'),
      delayHours: asString(j['delay_hours'], '0.00'),
      overtimeHours: asString(j['overtime_hours'], '0.00'),
      overtimeSalary: asString(j['overtime_salary'], '0.00'),
      deductions: asString(j['deductions'], '0.00'),
      bonuses: asString(j['bonuses'], '0.00'),
      additions: asString(j['additions'], '0.00'),
      advances: asString(j['advances'], '0.00'),
      finalNetEntitlement:
          asString(j['final_net_entitlement'] ?? j[ApiKey.total], '0'),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      ApiKey.employee_id: employeeId,
      ApiKey.employee_name: employeeName,
      ApiKey.salary: salary,
      ApiKey.debts: debts,
      ApiKey.points: points,
      ApiKey.hour_work_price: hourWorkPrice,
      ApiKey.total_work_hours: totalWorkHours,
      ApiKey.number_of_work_hours: numberOfWorkHours,
      ApiKey.points_revenue: pointsRevenue,
      ApiKey.total: total,
      'month': month,
      'selected_month': selectedMonth,
      'base_salary': baseSalary,
      'attendance_days': attendanceDays,
      'absent_days': absentDays,
      'late_days': lateDays,
      'delay_hours': delayHours,
      'overtime_hours': overtimeHours,
      'overtime_salary': overtimeSalary,
      'deductions': deductions,
      'bonuses': bonuses,
      'additions': additions,
      'advances': advances,
      'final_net_entitlement': finalNetEntitlement,
    };
  }
}
